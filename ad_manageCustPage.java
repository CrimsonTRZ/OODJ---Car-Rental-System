    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        FileHandling fh = new FileHandling("/Users/iankiew/NetBeansProjects/CarRentalSystem/src/TextFile/Car.txt");
        fh.readFile();
        this.fileContentSplit = fh.getfileContent();
        try {
            Status2.removeAllItems();
            for( int i = 0; i<4; i++){
                Status2.addItem(statuses[i]);
                if (Status2.getItemAt(i).contentEquals(this.bookingDetail[9])){
                    Status2.setSelectedIndex(i);
                }
            }
            carModel2.removeAllItems();
            carModel2.addItem("--Select Car Model--");
            for (int i = 1; i < fileContentSplit.size(); i++){
                content = fileContentSplit.get(i).get(0).toString();
                carModel2.addItem(content);
            } 
            for(int y = 0; y<carModel2.getItemCount(); y++){
                if(carModel2.getItemAt(y).equals(this.bookingDetail[5])){
                    carModel2.setSelectedIndex(y);
                }
            }
            custName2.setText(this.bookingDetail[1]);
            custIC2.setText(this.bookingDetail[2]);
            custContact2.setText(this.bookingDetail[3]);
            custMail2.setText(this.bookingDetail[4]);
            startDate2.setDate(df.parse(this.bookingDetail[6]));
            endDate2.setDate(df.parse(this.bookingDetail[7]));
            subtotal2.setText(this.bookingDetail[8]);
        } catch (ParseException ex) {
            Logger.getLogger(ad_manageCustPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        colCount = model.getColumnCount();
        FileHandling fh1 = new FileHandling("/Users/iankiew/NetBeansProjects/CarRentalSystem/src/TextFile/Booking.txt");
        fh1.readFile();
        this.fileContentSplit = fh1.getfileContent();
        for (int i = 1; i < fileContentSplit.size(); i++){
            for ( int y = 0; y < colCount; y++){
                content = fileContentSplit.get(i).get(y).toString();
                addtoRow[y] = content;
            }
            model.addRow(addtoRow);
        }
        
        if(bookingDetail != null){
            rowCount = custBookingTbl2.getRowCount();
            for(int i = 0; i< rowCount; i++){
                if(bookingDetail[0].equals(String.valueOf(model.getValueAt(i, 0)))){
                    selectRow = i;
                }
            }
            custBookingTbl2.setRowSelectionInterval(selectRow, selectRow);
        }
    }//GEN-LAST:event_formWindowOpened

