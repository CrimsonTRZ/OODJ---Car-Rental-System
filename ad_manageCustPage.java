    private void custBookingTbl2custBookingTblMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_custBookingTbl2custBookingTblMouseReleased
        try {
            selectRow = custBookingTbl2.getSelectedRow();
            this.bookingID = String.valueOf(model.getValueAt(selectRow, 0));
            custName2.setText(String.valueOf(model.getValueAt(selectRow, 1)));
            custIC2.setText(String.valueOf(model.getValueAt(selectRow, 2)));
            custContact2.setText(String.valueOf(model.getValueAt(selectRow, 3)));
            custMail2.setText(String.valueOf(model.getValueAt(selectRow, 4)));
            carMod = String.valueOf(model.getValueAt(selectRow, 5));
            for(int y = 0; y<carModel2.getItemCount(); y++){
                if(carModel2.getItemAt(y).contentEquals(carMod)){
                carModel2.setSelectedIndex(y);
                }
            }
            startDate2.setDate(df.parse(String.valueOf(model.getValueAt(selectRow, 6))));
            endDate2.setDate(df.parse(String.valueOf(model.getValueAt(selectRow, 7))));
            subtotal2.setText(String.valueOf(model.getValueAt(selectRow, 8)));
            String stat = String.valueOf(model.getValueAt(selectRow, 9));
            for( int i = 0; i<Status2.getItemCount(); i++){
                if (Status2.getItemAt(i).contentEquals(stat)){
                    Status2.setSelectedIndex(i);
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(ad_manageCustPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_custBookingTbl2custBookingTblMouseReleased

