    private void searchIconMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchIconMouseClicked
        // TODO add your handling code here:
        boolean searchFound = true;
        search = searchbar.getText();
        
        for (int y = 1; y < fileContentSplit.size(); y++){
            if(search.equals(fileContentSplit.get(y).get(0))){
                for (int i = 0; i<colCount; i++){
                    content = fileContentSplit.get(y).get(i).toString();
                    addtoRow[i] = content;
                }
                model.setRowCount(0);
                model.addRow(addtoRow);
                searchFound = true;
                break;
            }
            else{
                searchFound = false;
            }
        }
        
        if(!searchFound){
            JOptionPane.showMessageDialog(this, "Car Model does not exist. Please enter a valid Car Model.", "Error Message", JOptionPane.ERROR_MESSAGE);
            model.setRowCount(0);
            for (int i = 1; i < fileContentSplit.size(); i++){
                for ( int y = 0; y < colCount; y++){
                    content = fileContentSplit.get(i).get(y).toString();
                    addtoRow[y] = content;
                }
                model.addRow(addtoRow);
            }
        }
    }//GEN-LAST:event_searchIconMouseClicked

