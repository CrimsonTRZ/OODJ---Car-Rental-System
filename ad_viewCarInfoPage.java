    private void updateCarInfoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateCarInfoBtnActionPerformed
        row = carInfoTbl.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Please select a row.", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else {
            for (int i = 0; i < colCount; i++){
                selectedRow[i] = String.valueOf(model.getValueAt(row, i));
            }
            ad_manageCarPage manageCar = new ad_manageCarPage(user);
            manageCar.setTextFields(selectedRow);
            CommonFunction cf = new CommonFunction();
            cf.DisplayDisposeForm(manageCar, this);
        }
    }//GEN-LAST:event_updateCarInfoBtnActionPerformed

