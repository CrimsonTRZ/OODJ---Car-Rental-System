    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        ArrayList<List> fileData = new ArrayList<>();
        String[] columnHeaders = {"CARMODEL", "CARTYPE", "NUMOFSEATS", "RENTALPRICE(PERDAY)", "QUANTITY"};
        fileData.add(Arrays.asList(columnHeaders));
        for(int row = 0; row<carInfoTbl.getRowCount(); row++){
            String[] addToFile = new String[carInfoTbl.getColumnCount()];
            for (int col = 0; col<carInfoTbl.getColumnCount(); col++){
                String tableData = carInfoTbl.getValueAt(row, col).toString();
                addToFile[col] = tableData;
            }
            fileData.add(Arrays.asList(addToFile));
        }
        FileHandling fh = new FileHandling("/Users/iankiew/NetBeansProjects/CarRentalSystem/src/TextFile/Car.txt");
        fh.writeFile(fileData);
        JOptionPane.showMessageDialog(this, "Data Saved");
        CommonFunction cf = new CommonFunction();
        cf.DisplayDisposeForm(new ad_viewCarInfoPage(user), this);
    }//GEN-LAST:event_confirmBtnActionPerformed

