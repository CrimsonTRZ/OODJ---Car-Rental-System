    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        carModel.setText(this.carDetail[0]);
        carType.setText(this.carDetail[1]);
        numSeats.setText(this.carDetail[2]);
        rentPrice.setText(this.carDetail[3]);
        quantity.setText(this.carDetail[4]);
        
        colCount = model.getColumnCount();
        FileHandling fh = new FileHandling("/Users/iankiew/NetBeansProjects/CarRentalSystem/src/TextFile/Car.txt");
        fh.readFile();
        fileContentSplit = fh.getfileContent();
        for (int y = 1; y < fileContentSplit.size(); y++){
            for (int i = 0; i<colCount; i++){
            content = fileContentSplit.get(y).get(i).toString();
            addtoRow[i] = content;
            }
             model.addRow(addtoRow);
        }
        
        if(carDetail[0] != null){
            rowCount = carInfoTbl.getRowCount();
            for(int i = 0; i< rowCount; i++){
                if(carDetail[0].equals(String.valueOf(model.getValueAt(i, 0)))){
                    selectRow = i;
                }
            }
            carInfoTbl.setRowSelectionInterval(selectRow, selectRow);
        }
    }//GEN-LAST:event_formWindowOpened

