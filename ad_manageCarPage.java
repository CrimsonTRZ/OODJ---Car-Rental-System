    private void addCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCarActionPerformed
        //private String carName, carClass, noSeats, price, quan;
        carName = carModel.getText();
        carClass = carType.getText();
        noSeats = numSeats.getText();
        price = rentPrice.getText();
        quan = quantity.getText();

        String[] integerFields = {noSeats, price, quan};
        String [] values = {carName, carClass, noSeats, price, quan};
        ValidateFields vf = new ValidateFields();
        boolean validIntegers = vf.ValidateInteger(integerFields);
        CommonFunction cf = new CommonFunction();
        rowCount = carInfoTbl.getRowCount();
        for(int i = 0; i<rowCount; i++){
            if(carName.equals(String.valueOf(model.getValueAt(i, 0)))){
                sameTxt = true;
                break;
            }
            else {
                sameTxt = false;
            }
        }

        if(!cf.checkEmpty(values) && validIntegers){
            if (sameTxt){
                JOptionPane.showMessageDialog(this, "<html>Car Model <b>MUST</b> be <b>UNIQUE</b>.<html>", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
            else{
                model.addRow(values);
                carModel.setText("");
                carType.setText("");
                numSeats.setText("");
                rentPrice.setText("");
                quantity.setText("");
            }
        }

    }//GEN-LAST:event_addCarActionPerformed

    private void editCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCarActionPerformed
        carName = carModel.getText();
        carClass = carType.getText();
        noSeats = numSeats.getText();
        price = rentPrice.getText();
        quan = quantity.getText();

        String[] integerFields = {noSeats, price, quan};
        String [] values = {carName, carClass, noSeats, price, quan};
        ValidateFields vf = new ValidateFields();
        boolean validIntegers = vf.ValidateInteger(integerFields);
        CommonFunction cf = new CommonFunction();
        if(validIntegers && cf.checkEmpty(values) == false){
            if (selectRow != -1){
                model.removeRow(selectRow);
                model.insertRow(selectRow, values);

                carModel.setText("");
                carType.setText("");
                numSeats.setText("");
                rentPrice.setText("");
                quantity.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Please Select a Row to Modify.", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_editCarActionPerformed

